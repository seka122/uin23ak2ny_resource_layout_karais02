function openPage(id) {
    // Denne går gjennom listen av ressurser som er beskrevet ovenfor, denne går gjennom elemenetene i listen//
    let TextContent = ``;
    resources.map
    ((content) => {
    if (id === content.category) {
       // Denne lager et meny punkt, det legges en knapp for kategoriene og en undermeny som åpner når du trykker på knappen, undermenyen inneholder tekster
    TextContent += `
    <h2 class="category_title">${content.category}</h2>
    <p class="textInfo">${content.text}
        ${content.sources.map(source =>`
            <li>
                <a class="links" href="${source.url}">${source.title}</a>
            </li>
        </p>`)}     
    `}})

    //Denne skiver ut i htmlen
    document.getElementById("content").innerHTML=TextContent

}




